<template>
	<view class="spread">
		<view class="title">{{qr_poster.title}}</view>
		<view class="er">
			<img :src="qr_poster.poster"></img>
		</view>
		<view class="btn">
			<view>
				<button class="butt" @click="save" :disabled="is_click">保存二维码</button>
			</view>
		</view>
		<view class="gui">
			<view>1.将此页面二维码保存并发送给您的好友</view>
			<view>2.好友进行采购后返推荐奖励给您账户</view>
		</view>
	</view>
</template>

<script>
	export default {
		data() {
			return {
				is_click:false,
				qr_poster:'',
			};
		},
		onLoad() {
			uni.showLoading({
				title: '加载中'
			});
			this.$api.CC_request.get_user_dis_qrcode().then(res => {
				this.qr_poster = res[0]
				uni.hideLoading();
			})
		},
		methods: {
			save() {
				// #ifdef H5
				uni.showModal({
					title:'请截图保存',
				})
				// #endif
				
				// #ifdef MP-WEIXIN
				const that = this
				this.is_click = true
				uni.showLoading({
					title: '加载中'
				});
				var url = this.qr_poster.poster
				uni.authorize({
					scope: 'scope.writePhotosAlbum',
					success(res) {
						uni.downloadFile({
							url: url,
							success: (res) => {
								console.log(res)
								uni.saveImageToPhotosAlbum({
									filePath: res.tempFilePath,
									success: function(res) {
										console.log('save success', res);
										uni.showToast({
											title: '图片保存成功',
											duration: 2000
										})
										that.is_click = false
										uni.hideLoading();
									}
								});
							}
						});
				
					}
				})
				
				// #endif

				

			},
		},
		onPullDownRefresh() {
			//this._load()
			setTimeout(function() {
				uni.stopPullDownRefresh();
			}, 2000);
		}
	}
</script>

